package com.example.jse.m02.s12.ex;

/**
 * Control flow - Loop: for
 */
public class Exercise1 {
    /**
     * Print all values in [0 .. 9]
     * 
     * @param args not used
     */
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
    }
}
